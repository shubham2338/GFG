class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        int s=0;
        for(auto e:A)s+=e;
        int s1=0;
        int ok=0;
        for(int i=0;i<N;++i){
            if(i>0){
                s1+=A[i-1];
            }
            s-=A[i];
            if(s1==s)ok=i+1;
        }
        if (ok!=0) return ok;
        return -1;
    }
};


